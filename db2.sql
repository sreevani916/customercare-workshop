CREATE TABLE ORDERS ( 

	ORDER_ID VARCHAR(50) NOT NULL,

	NAME VARCHAR(50),

	CITY VARCHAR(50),

	STATE VARCHAR(50),

	COUNTRY VARCHAR(50),

	ADDRESS_LINE1 VARCHAR(50),

	ZIPCODE VARCHAR(50),

	PHONE VARCHAR(50),

	STATUS VARCHAR(36),

	PRIMARY KEY (ORDER_ID)

)ORGANIZE BY ROW;





